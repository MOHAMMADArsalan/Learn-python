
num1 = 10;
num2 = 20;

# addition 
add =  num1 + num2;
print(add);

# subtraction 
sub = num1 - num2;
print(sub);

# Multipication
mul = num1 * num2;
print(mul)

# Division With Float Number
division = num1 / num2; # if you use one forward slash its return result in float number
print(division);

# Division Without Float Number
division = num1 // num2;
print(division);

# modulo

mod = num1 % num2;
print(mod);

# Exponentiation 
exp = 2 ** 3;
print(exp)







